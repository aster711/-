export function getRandomYoumuDialogue(
  category: keyof typeof YOUMU_EXCLUSIVE_DIALOGUES,
  score?: number
): string {
  const list = YOUMU_EXCLUSIVE_DIALOGUES[category];
  const selected = list[Math.floor(Math.random() * list.length)];
  if (score !== undefined) {
    // Replace circles with score
    return selected.replace(/○/g, score.toString());
  }
  return selected;
}

