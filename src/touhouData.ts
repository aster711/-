export function getRandomReimuDialogue(
  category: keyof typeof REIMU_EXCLUSIVE_DIALOGUES,
  score?: number
): string {
  const list = REIMU_EXCLUSIVE_DIALOGUES[category];
  const selected = list[Math.floor(Math.random() * list.length)];
  if (score !== undefined) {
    // Replace circles with score
    return selected.replace(/○/g, score.toString());
  }
  return selected;
}

