export function getRandomSanaeDialogue(
  category: keyof typeof SANAE_EXCLUSIVE_DIALOGUES,
  score?: number
): string {
  const list = SANAE_EXCLUSIVE_DIALOGUES[category];
  const selected = list[Math.floor(Math.random() * list.length)];
  if (score !== undefined) {
    return selected.replace(/○/g, score.toString());
  }
  return selected;
}




