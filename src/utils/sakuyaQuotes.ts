export function getRandomSakuyaQuote(category: SakuyaCategory): string {
  const list = SAKUYA_QUOTES[category];
  if (!list || list.length === 0) return 'それでは、始めましょうか。';
  return list[Math.floor(Math.random() * list.length)];
}
